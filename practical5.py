# Declaring a String
str = "Hello World"
print(str)

# lower()
print(str.lower())

# upper()
print(str.upper())

# join()
lst = ["This", "is", "a", "string"]
print(" ".join(lst))

# split()
lst = str.split(" ")
print(lst)

# find()
print(str.find("llo"))

# replace()
print(str.replace("World", "Python"))
