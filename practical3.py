# Creation of tuple in python
tu = (1,2,3,3,2,4,5,6)
print(tu)

# count(), counts the provided element
print(tu.count(3))

# index(), returns the index number of provided element
print(tu.index(5))

# Slicing experiments:
# (0,1,2,3,4,5) : Positive Way indexing
# (-6,-5,-4,-3,-2,-1) : Negative Way indexing
# slicing operator=" : ", slicing=[Initial Index number: until Index number]

print(tu[1:-1])
